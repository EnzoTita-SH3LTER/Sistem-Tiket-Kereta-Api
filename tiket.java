    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Nama Penumpang");
        model.addColumn("Alamat");
        model.addColumn("Stasiun Keberangkatan");
        model.addColumn("Stasiun Akhir");
        model.addColumn("Waktu");
        model.addColumn("nama Kereta");
        model.addColumn("Harga Tiket");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM tiket ";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res =  stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7)});
                
            }
            
            tabelTiket.setModel(model);
            
        } catch (SQLException e){
            System.out.println ("Error : " +e.getMessage());
            
        }
    }
    
    
