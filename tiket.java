    private void tabelTiketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelTiketMouseClicked
        // TODO add your handling code here:
        //prosedur edit data
        int baris = tabelTiket.rowAtPoint(evt.getPoint());
        String nama = tabelTiket.getValueAt(baris, 1).toString();
        txtNama.setText(nama);
        
        String alamat = tabelTiket.getValueAt(baris, 2).toString();
        txtAlamat.setText(alamat);
        
        String keberangkatan = tabelTiket.getValueAt(baris, 3).toString();
        cbKeberangkatan.setSelectedItem(keberangkatan);
        
        String tiba = tabelTiket.getValueAt(baris, 4).toString();
        cbTiba.setSelectedItem(tiba);
        
        String waktu = tabelTiket.getValueAt(baris, 5).toString();
        cbWaktu.setSelectedItem(waktu);
        
        String kereta = tabelTiket.getValueAt(baris, 6).toString();
        cbNamaKereta.setSelectedItem(kereta);
        
        String harga = tabelTiket.getValueAt(baris, 7).toString();
        cbHarga.setSelectedItem(harga);
    }//GEN-LAST:event_tabelTiketMouseClicked

